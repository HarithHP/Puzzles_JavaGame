     public static String convertToString(int[][] array) {
        StringBuilder stringBuilder = new StringBuilder();

        for (int[] row : array) {
            for (int element : row) {
                stringBuilder.append(element).append(" ");
            }
            stringBuilder.append("\n");
        }

        return stringBuilder.toString().trim();
    }
